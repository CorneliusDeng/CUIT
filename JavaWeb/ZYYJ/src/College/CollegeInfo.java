package College;

//学院管理值Bean
public class CollegeInfo 
{
	private String collegid;
	private String collegename;
	private String collegedetail;
	public String getCollegid() 
	{
		return collegid;
	}
	public void setCollegid(String collegid) 
	{
		this.collegid = collegid;
	}
	public String getCollegename() 
	{
		return collegename;
	}
	public void setCollegename(String collegename) 
	{
		this.collegename = collegename;
	}
	public String getCollegedetail() 
	{
		return collegedetail;
	}
	public void setCollegedetail(String collegedetail) 
	{
		this.collegedetail = collegedetail;
	}
	
}
