import numpy as np

x1 = np.array(input().split(','), float)
y1 = np.array(input().split(','), float)
x2 = np.array(input().split(','), float)
y2 = np.array(input().split(','), float)
X = np.array(input().split(','), float)
w1 = np.c_[x1, y1]
w2 = np.c_[x2, y2]
cov1 = np.cov(w1.T)
mean1 = w1.mean(axis=0)
cov2 = np.cov(w2.T)
mean2 = w2.mean(axis=0)
w = cov1 + cov2
s = np.linalg.inv(w)
c = np.dot(s, mean1 - mean2)
r = np.dot(c, X - (mean1 + mean2) / 2)
p = np.log(0.6 / 0.4)
t = '该点属于第一类' if r > p else '该点属于第二类'
